#include <unistd.h>

int	ft_strlen(char	*str)
{
	int	i = 0;
	while (str[i])
		i++;
	return (i);
}

int main(int ac, char *av[])
{
	int l = ft_strlen(av[1])  - 1;
	if (ac == 2)
	{
		while (l >= 0)
		{
			write (1, &av[1][l],1);
			l--;
		}
	}
	write(1, "\n", 1);
}
