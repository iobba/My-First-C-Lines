#include <unistd.h>

int main(int ac, char *av[])
{
	int i =0;
	char *p;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if (i == 0 && av[1][i] > 32)
				p = &av[1][i];
			else if (av[1][i] <= 32 && av[1][i + 1] > 32)
				p = &av[1][i + 1];
			i++;
		}
		i = 0;
		while (p && p[i] > 32)
		{
			write(1, &p[i], 1);
			i++;
		}
	}
	write (1, "\n", 1);
}
